// include the library first of all
#include <iostream> 
using namespace std;
// to define the values of the variables
int main(){
	int a = 2;
	int b = 3;
	float c = 5.31;
	float d = 45.67; 
// to show the audiences the values of my variables
	cout << " a = " << a << endl;
	cout << " b = " << b << endl;
	cout << " c = " << c << endl;
	cout << " d = " << d << endl;
// to declare the arithmatic symbols
	int add = a+b;
	int sub = a-b;
	float mul = b*c;
	float div = c/d;
	int mod = a%b;
// to perform the arithmatics operations on variables
	cout << " a+b = " << add << endl;
	cout << " a-b = " << sub << endl;
	cout << " b*c = " << mul << endl;
	cout << " c/d = " << div << endl;
	cout << " a%b = " << mod << endl;
return 0;
}
